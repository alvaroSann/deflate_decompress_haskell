-- | Фиксированные длины кодов для литералов и длин (RFC 1951, раздел 3.2.6):
-- Коды 0..143     -> длина 8 бит
-- Коды 144..255   -> длина 9 бит
-- Коды 256..279   -> длина 7 бит
-- Коды 280..287   -> длина 8 бит
fixedLitLenLengths :: [Int]
fixedLitLenLengths = concat 
    [ replicate 144 8
    , replicate 112 9
    , replicate 24  7
    , replicate 8   8
    ]

-- | Статическое дерево Хаффмана для литералов и длин
fixedLitLenTree :: HuffmanTree
fixedLitLenTree = safeBuildTree fixedLitLenLengths "fixed literal/length tree"

-- | Статическое дерево Хаффмана для расстояний (все 32 кода имеют длину 5 бит)
fixedDistTree :: HuffmanTree
fixedDistTree = safeBuildTree (replicate 32 5) "fixed distance tree"

-- | Вспомогательная функция для безопасного развертывания дерева при инициализации.
safeBuildTree :: [Int] -> String -> HuffmanTree
safeBuildTree lengths treeName = 
    case buildTree lengths of
        Just tree -> tree
        Nothing   -> error $ "Critical: Failed to initialize static DEFLATE " ++ treeName
